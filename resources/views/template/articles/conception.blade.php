                     <p class="excert">
                       Cette approche expansive du design - et l'impact d'un designer - se reflète dans le thème sombre de Material , une interface à faible luminosité sur laquelle j'ai travaillé qui affiche principalement des surfaces sombres pour prendre en charge l'utilisation de l'écran dans les environnements sombres. <br>
                       Un autre exemple peut être vu dans les explorations de l'équipe sur le bien - être numérique , un projet qui prend le bien-être physique et mental comme une exigence centrale pour les expériences numériques. <br>
                       <br>
                       En regardant en arrière sur ces projets, je peux voir qu'ils ont été enrichis par l'éthique du design - les principes moraux qui régissent les décisions et les objectifs d'un designer. <br>
                       Lorsque j'ai travaillé sur le thème sombre de Material et les projets de bien-être numérique, je n'avais pas le vocabulaire, les connaissances ou la perspective pour articuler les opportunités et les tensions éthiques dans le travail. <br>
                       <br>
                       Ce n'est que lorsque j'ai consacré du temps à acquérir de manière indépendante une compréhension plus approfondie de l'éthique du design que j'ai pu reconnaître et répondre à la responsabilité sociale du design. <br>

                       J'ai ralenti pour comprendre mon rôle de designer. J'ai commencé à considérer mes contributions non pas comme un ensemble de tâches et de délais, mais comme une extension de mon rôle social plus large. <br>
                       Travailler sur le système de conception de Material, j'ai réalisé, c'est participer simultanément à un système beaucoup plus vaste. <br>
                       <br>

                       Chaque designer définit son rôle différemment, souvent en fonction du type de travail qu'il effectue, comment il le fait et pour qui il le fait. <br>
                       Mais, quelle que soit la manière dont un designer comprend son rôle, les impacts sociaux et culturels de l'œuvre sont inévitables. <br>
                       Cela signifie que nous devons faire de la place dans la définition du travail d'un designer pour inclure des pratiques responsables qui tiennent compte de notre impact. <br>
                     </p>

                     <div class="quote-wrapper">
                        <div class="quotes">
                          <p>
                            <i>Faire un choix</i> <br>
                            La chose la plus importante que j'ai apprise sur l'éthique du design est que ce que je choisis de faire ou de ne pas faire est une déclaration de mon éthique. <br>
                            Un choix est un rappel que les décisions percutantes doivent être prises avec une profonde conscience. <br>
                            Certains des choix qui se produisent tout au long du processus de conception comprennent: <br>
                            <br>
                            Comprendre activement les problèmes dans le contexte plus large d'une société en constante évolution <br>
                            Demander pourquoi quelque chose est comme ça <br>
                            Comprendre le contexte original d'une «meilleure pratique» et la reconnaître peut ne pas s'appliquer dans un nouveau contexte <br>
                            Considérer toute personne ou tout lieu qu'un projet peut affecter comme partie prenante <br>
                            Ne pas déclasser les divers besoins humains ou environnementaux comme des cas extrêmes indignes d'attention ou d'investissement <br>
                            Eviter de renforcer les normes établies à travers les récits culturels dominants <br>
                          <p>
                        </div>
                     </div>
                     <p>
                       <i>Espace pour apprendre</i> <br>
                       <br>
                       Faire du temps et de l'espace pour apprendre et appliquer la pensée éthique est optimiste. <br>
                       Il est rarement prévu dans les délais des projets et est largement absent de la formation en design. Il est peu probable que quelqu'un vous demande de lire et de réfléchir à l'éthique, ce qui place la responsabilité d'apprendre sur les concepteurs individuels. <br>
                       Plus j'apprends, plus je ressens d'humilité. En élargissant notre exposition à la pensée éthique, nous pouvons mieux comprendre qui nous sommes, ce que nous pourrions et devrions faire et pourquoi nous faisons les choses que nous faisons. <br>
                       <br>
                       Avec cette conscience de soi, nous sommes dans un bien meilleur endroit pour comprendre où nous pouvons être le plus efficace dans la conception. <br>
                       Il n'y a pas de moyen unique d'appliquer l'éthique à une pratique de conception; peut-être que la seule règle est de regarder constamment vers l'avenir ainsi que vers le passé lors de la conception. <br>
                       Nos décisions sont éclairées par le passé; et parce que nos décisions ont un impact, elles éclairent également des futurs qui méritent d'être anticipés de manière proactive. <br>
                       L'évaluation des héritages et l'anticipation des résultats des travaux de conception peuvent entraîner des changements significatifs du statu quo et peuvent être un acte d'espoir.
