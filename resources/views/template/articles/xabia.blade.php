                        mais se mettre dans la peau de l'utilisateur et imaginer son cheminement, tester et trouver des solutions font parties des stratégies d'un bon web designer. <br>
                        <br>
                        Pour commencer, je choisis 3 types de palettes de manière simpliste pour accrocher mon thème: les vacances d'été.
                        Je m'oriente vers les couleurs chaudes: le jaune pour le soleil, la luminosité, la joie, le bleu pour le ciel, la mer et la confiance, le rouge pour la chaleur, l'amour,
                        le vert pour la nature apaissante. <br>
                        <br>
                        Je choisis également la typographie associant avec la marque, le logo ou encore le thème. <br>
                        La typographie a une signification bien particulière dans le contexte de location de logement. <br>
                        D'une part, elle doit se démarquer par un certain style de modernité. D'autre part, elle doit prouver son confort, sa confiance pour sa clientèle. <br>
