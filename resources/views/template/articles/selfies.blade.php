                     <p class="excert">
                       <i>Education et sensibilisation</i> <br>
                       Les effets de la retouche subtile du visage sont plus faciles à repérer en comparant des images fixes, plutôt que par des flux de caméra en temps réel.
                       Il est encore plus facile de comparer des images fixes lorsqu'elles sont sur le même écran, mais c'est rarement une option. Pour compliquer les choses,
                       le flux de la caméra ne représente pas toujours avec précision l'aspect final de l'image, ce qui conduit certains à confondre le niveau de retouche du visage qu'ils recherchent.
                       Une façon de créer des moments éducatifs est de faciliter la comparaison post-capture d'images fixes.

                     </p>
                     <p>
                       Nos recherches suggèrent que de nombreuses personnes apprécieraient les applications qui fournissent plus d'informations
                       sur le lien entre la retouche du visage et le bien-être.
                       C'est là que la recommandation en matière d'éducation entre en jeu.
                       Elle se rapporte aux principes de bien-être numérique de sensibilisation et d'adaptabilité,
                       qui suggèrent que les expériences contextuelles aident à s'adapter aux divers objectifs,
                       capacités et besoins des gens.
                     </p>
                     <div class="quote-wrapper">
                        <div class="quotes">
                          Le but des utilisateurs est de privilégier une image de soi positive. <br>
                          Les concepteurs et les développeurs peuvent créer des expériences qui affectent les gens dans
                          toutes les dimensions de leur vie. Que ce soit pour jouer, travailler, faire de l'exercice,
                          se détendre ou apprendre, atteindre son état de bien-être est un voyage constant.
                          Étant donné que prendre des photos est l'une des choses les plus courantes que nous faisons avec nos téléphones, donner la priorité à une image de soi positive à l'aide des techniques décrites ici permettra à ces expériences d'améliorer, plutôt que de nuire, au bien-être d'une personne.
                        </div>
                     </div>
                     <p>
                       <i>Transparence</i>
                       Les fonctionnalités de retouche faciale peuvent être subtiles, ce qui rend difficile de savoir quand les images sont modifiées.
                       Les gens peuvent ne pas être conscients de l'état par défaut de la caméra ou oublier avec le temps quel paramètre est actuellement actif.

                     <p>
                       Les aperçus en temps réel ne communiquent pas toujours non plus l'état actuel d'un paramètre de retouche de visage. Tous ces facteurs peuvent conduire à de la détresse lorsque les gens voient leur apparence intacte.
                       L'American Academy of Facial Plastic and Reconstructive Surgery note que 72% de leurs membres en 2019 ont vu des patients demander une chirurgie esthétique pour améliorer leurs selfies - une augmentation de 15% par rapport à l'année précédente. <br>
                       Cela rend la transparence des paramètres de retouche de visage particulièrement importante.
                       La transparence signifie prendre des mesures pour indiquer clairement quand la retouche du visage est appliquée, à la fois dans le flux de la caméra en temps réel et après la capture d'une image.
                     </p>
                     </p>
